# ==============================================================================
# ОЧИЩЕННЯ ДАНИХ ВІД ПРОПУСКІВ: МЕТОД DROPNA() У PANDAS
# ==============================================================================

import pandas as pd
import numpy as np

# Перевіряємо версію pandas для контролю передбачуваності методів
print(f"Версія Pandas у вашому середовищі: {pd.__version__}\n")

# Завантажуємо оригінальний брудний датасет пасажирів Титаніка з відкритого джерела
# Цей датасет містить ідеальне поєднання числових та категоріальних пропусків (NaN)
df_url = "https://raw.githubusercontent.com/datasciencedojo/datasets/master/titanic.csv"
df = pd.read_csv(df_url)

print("--- Оригінальний датасет завантажено ---")
print(f"Початковий розмір таблиці: {df.shape[0]} рядків на {df.shape[1]} стовпців.")

# ==============================================================================
# ЧАСТИНА 1: ПЕРВИННА ВАЛІДАЦІЯ ТА СТАН ПРОПУСКІВ (MISSING VALUES ANALYSIS)
# ==============================================================================
print("\n==============================================================================")
print("ЧАСТИНА 1: Аналіз пропущених значень у стовпцях")
print("==============================================================================")

# Метод .isnull().sum() показує точну кількість NaN для кожної колонки
print("Кількість NaN у кожному стовпці:")
print(df.isnull().sum())

# ==============================================================================
# ЧАСТИНА 2: ОЦІНКА МАСШТАБІВ ТА ПАРАМЕТР how ('any' проти 'all')
# ==============================================================================
print("\n==============================================================================")
print("ЧАСТИНА 2: Радикальне очищення проти делікатного (.shape валідація)")
print("==============================================================================")

# Варіант А: Радикальний метод how='any' (Працює за замовчуванням)
# Видаляє рядок, якщо в ньому є хоча б одна пуста клітинка в будь-якому місці.
print(f"Було рядків спочатку: {df.shape[0]}")
print(f"Залишиться після df.dropna(): {df.dropna().shape[0]} рядків.")
print("Порада: Ми втратили майже 80% даних! Метод dropna() без параметрів є небезпечним.")

# Варіант Б: Обережний метод how='all'
# Видаляє рядок ЛИШЕ тоді, коли ВСІ стовпці у цьому рядку є NaN (порожні технічні рядки)
print(f"Залишиться після df.dropna(how='all'): {df.dropna(how='all').shape[0]} рядків.")

# ==============================================================================
# ЧАСТИНА 3: ТОЧКОВЕ ОЧИЩЕННЯ ЗА ДОПОМОГОЮ ПАРАМЕТРА subset
# ==============================================================================
print("\n==============================================================================")
print("ЧАСТИНА 3: Очищення за конкретними критично важливими стовпцями (subset)")
print("==============================================================================")

# Уявіть, що для аналізу нам критично мати дані про місто посадки ('Embarked') та вік ('Age').
# Якщо вони порожні — рядок нам не потрібен. Решту стовпців (наприклад, Cabin) ми не чіпаємо.

# condition: how='any' (за замовчуванням у subset) -> видалить якщо ХОЧА Б ОДИН із цих двох стовпців NaN
df_subset_any = df.dropna(subset=['Age', 'Embarked'])
print(f"Розмір після dropna(subset=['Age', 'Embarked']): {df_subset_any.shape[0]} рядків.")

# Текстова перевірка технічного стану залишку через .info()
print("\nТехнічний стан таблиці після фільтрації за subset:")
df_subset_any.info()

# ==============================================================================
# ЧАСТИНА 4: ФІЛЬТРАЦІЯ ТА ОЧИЩЕННЯ СТОВПЦІВ ЗА ПОРОГОМ ЗАПОВНЕНОСТІ (thresh)
# ==============================================================================
print("\n==============================================================================")
print("ЧАСТИНА 4: Розрахунок порогу заповненості (thresh) для видалення стовпців")
print("==============================================================================")

# Якщо стовпець пустий на половину або більше — він некорисний, його треба прибрати.
# Рахуємо поріг: 50% від загальної кількості рядків таблиці
missing_threshold = int(df.shape[0] * 0.50)
print(f"Розрахований ліміт (50% від таблиці): {missing_threshold} дійсних значень.")

# Використовуємо axis=1 (стовпці) та thresh=missing_threshold.
# Це означає: "Залишити стовпець ТІЛЬКИ якщо в ньому є ЯК МІНІМУМ 445 заповнених клітинок".
df_cleaned_cols = df.dropna(axis=1, thresh=missing_threshold)

print(f"\nКількість стовпців БУЛО: {df.shape[1]}")
print(f"Кількість стовпців СТАЛО після фільтрації thresh: {df_cleaned_cols.shape[1]}")
print(f"Видалені стовпці, що мали понад 50% пропусків: {set(df.columns) - set(df_cleaned_cols.columns)}")

# ==============================================================================
# ЧАСТИНА 5: ФІКСАЦІЯ ЗМІН (inplace=True В ДІЇ)
# ==============================================================================
print("\n==============================================================================")
print("ЧАСТИНА 5: Фіксація очищення в оригінальному DataFrame")
print("==============================================================================")

# Створимо копію для безпечної демонстрації
df_final = df.copy()

# Видаляємо повністю порожні рядки (якщо такі були в кінці файлу)
# Параметр inplace=True перезаписує df_final безпосередньо в пам'яті
df_final.dropna(how='all', inplace=True)

print("Зміни успішно зафіксовано «на місці». Датафрейм готовий до моделювання та аналізу!")
print("==============================================================================")